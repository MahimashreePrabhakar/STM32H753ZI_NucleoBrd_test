{
char *command = strtok((char *)rxBuffer, "_");
char *valueStr = strtok(NULL, "_");

    if (command && valueStr)
    {
int value = atoi(valueStr);
if (strncmp(command, "PWM", 3) == 0)
        {
int pinIndex = atoi(command + 3);
            updatePWMValue(pinIndex, value);
        }
else if (strncmp(command, "GPIO", 4) == 0)
        {
int pinIndex = atoi(command + 4);
            setGPIOState(pinIndex, value != 0 ? GPIO_PIN_SET : GPIO_PIN_RESET);
        }
else
        {
printf("Unknown command\n");
        }
    }
}


