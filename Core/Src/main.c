    	case 3: __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_4, pwmValue);break;//Feed 1 pwm
    	case 4: __HAL_TIM_SET_COMPARE(&htim12, TIM_CHANNEL_2, pwmValue);break;//Feed 2 pwm
    	case 5: __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, pwmValue);break;//L1 pwm
    	case 6: __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_4, pwmValue);break;//L2 pwm
    	case 7: __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pwmValue);break;//L3 pwm
    	case 8: __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, pwmValue);break;//L4 pwm

